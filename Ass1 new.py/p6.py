int_num = 10
float_num = float(int_num)
print(f"Integer {int_num} converted to float: {float_num}")

# Float to integer
float_val = 3.99
int_val = int(float_val)
print(f"Float {float_val} converted to integer: {int_val}")

# Integer to string
str_num = str(int_num)
print(f"Integer {int_num} converted to string: '{str_num}'")

# String to integer
str_val = "25"
int_from_str = int(str_val)
print(f"String '{str_val}' converted to integer: {int_from_str}")

# String to float
float_from_str = float(str_val)
print(f"String '{str_val}' converted to float: {float_from_str}")
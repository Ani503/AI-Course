# Arithmetic Operators
a = 10
b = 3
print("Arithmetic Operators:")
print(f"a + b = {a + b}")
print(f"a - b = {a - b}")
print(f"a * b = {a * b}")
print(f"a / b = {a / b}")
print(f"a % b = {a % b}")
print(f"a ** b = {a ** b}")
print(f"a // b = {a // b}")

# Assignment Operators
print("\nAssignment Operators:")
x = 5
print(f"x = {x}")
x += 2
print(f"x += 2: {x}")
x *= 3
print(f"x *= 3: {x}")

# Conditional (Comparison) Operators
print("\nConditional (Comparison) Operators:")
print(f"a == b: {a == b}")
print(f"a != b: {a != b}")
print(f"a > b: {a > b}")
print(f"a < b: {a < b}")
print(f"a >= b: {a >= b}")
print(f"a <= b: {a <= b}")

# Identity Operators
print("\nIdentity Operators:")
y = a
print(f"a is y: {a is y}")
print(f"a is not b:{a is not b}")
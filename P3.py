# Define two complex numbers
c1 = 4 + 3j
c2 = 2 + 5j

# Perform addition
add_result = c1 + c2

# Perform subtraction
sub_result = c1 - c2

# Display results
print("Complex Number Operations:")
print(f"First complex number: {c1}")
print(f"Second complex number: {c2}")
print(f"Addition: {c1} + {c2} = {add_result}")
print(f"Subtraction: {c1} - {c2} = {sub_result}")